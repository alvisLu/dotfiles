-- 設定 vue 和 volar
-- https://github.com/neovim/nvim-lspconfig/blob/master/doc/server_configurations.md#volar
-- 使用 volar 需要指定 typescript 的路徑。
-- 可以先設定 init_options.typescript.tsdk (但使用 monorepo 會有問題)。
-- on_new_config function 是 monorepo 的解決方法:
local function on_new_config(new_config, new_root_dir)
  local function get_typescript_server_path(root_dir)
    -- 如果 project 下沒安裝 typescript，就要指定 global ts 路徑
    local global_ts = '/Users/alvis/.nvm/versions/node/v16.13.0/lib/node_modules/typescript/lib'

    local project_root = lsp_util.find_node_modules_ancestor(root_dir)
    return project_root and
        (lsp_util.path.join(project_root, 'node_modules', 'typescript', 'lib'))
        or
        global_ts
  end

  if
      new_config.init_options
      and new_config.init_options.typescript
      and new_config.init_options.typescript.tsdk == ''
  then
    new_config.init_options.typescript.tsdk = get_typescript_server_path(new_root_dir)
  end
end

